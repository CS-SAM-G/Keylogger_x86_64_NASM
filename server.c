static const uint8_t r_con[10] = {
    0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36
};

static const uint8_t key[16] = {
    0x00,0x01,0x02,0x03,
    0x04,0x05,0x06,0x07,
    0x08,0x09,0x0a,0x0b,
    0x0c,0x0d,0x0e,0x0f
};

void copy_block(uint8_t *dst, const uint8_t *src) {
    for (int i = 0; i < 16; i++)
        dst[i] = src[i];
}

void add_round_key(uint8_t *state, const uint8_t *round_key) {
    for (int i = 0; i < 16; i++)
        state[i] ^= round_key[i];
}

void inv_sub_bytes(uint8_t *state) {
    for (int i = 0; i < 16; i++)
        state[i] = invS[state[i]];
}

void inv_shift_rows(uint8_t *state) {
    uint8_t tmp;

    tmp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = tmp;

    tmp = state[2];
    state[2] = state[10];
    state[10] = tmp;

    tmp = state[6];
    state[6] = state[14];
    state[14] = tmp;

    tmp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = tmp;
}

static uint8_t mul(uint8_t a, uint8_t b) {
    uint8_t r = 0;

    while (b) {
        if (b & 1)
            r ^= a;

        uint8_t hi = a & 0x80;

        a <<= 1;

        if (hi)
            a ^= 0x1b;

        b >>= 1;
    }

    return r;
}

void inv_mix_columns(uint8_t *state) {
    for (int c = 0; c < 4; c++) {
        uint8_t *col = &state[c * 4];

        uint8_t a0 = col[0];
        uint8_t a1 = col[1];
        uint8_t a2 = col[2];
        uint8_t a3 = col[3];

        col[0] = mul(a0, 0x0e) ^ mul(a1, 0x0b) ^ mul(a2, 0x0d) ^ mul(a3, 0x09);
        col[1] = mul(a0, 0x09) ^ mul(a1, 0x0e) ^ mul(a2, 0x0b) ^ mul(a3, 0x0d);
        col[2] = mul(a0, 0x0d) ^ mul(a1, 0x09) ^ mul(a2, 0x0e) ^ mul(a3, 0x0b);
        col[3] = mul(a0, 0x0b) ^ mul(a1, 0x0d) ^ mul(a2, 0x09) ^ mul(a3, 0x0e);
    }
}

void g_function(uint8_t *word, uint8_t rcon) {
    uint8_t tmp = word[0];

    word[0] = word[1];
    word[1] = word[2];
    word[2] = word[3];
    word[3] = tmp;

    for (int i = 0; i < 4; i++)
        word[i] = S[word[i]];

    word[0] ^= rcon;
}

void key_expansion(const uint8_t *k, uint8_t *expanded) {
    uint8_t temp[4];

    for (int i = 0; i < 16; i++)
        expanded[i] = k[i];

    for (int i = 4; i < 44; i++) {
        for (int j = 0; j < 4; j++)
            temp[j] = expanded[4 * (i - 1) + j];

        if (i % 4 == 0)
            g_function(temp, r_con[i / 4 - 1]);

        for (int j = 0; j < 4; j++)
            expanded[4 * i + j] =
                expanded[4 * (i - 4) + j] ^ temp[j];
    }
}

void decrypt_block(uint8_t *state, const uint8_t *expanded_key) {
    add_round_key(state, expanded_key + 16 * 10);

    for (int round = 9; round >= 1; round--) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, expanded_key + 16 * round);
        inv_mix_columns(state);
    }

    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, expanded_key);
}

int decrypt_buffer(
    const uint8_t *in,
    uint8_t *out,
    int len,
    const uint8_t *expanded_key
) {
    if (len % AES_BLOCK_SIZE != 0)
        return -1;

    for (int i = 0; i < len; i += AES_BLOCK_SIZE) {
        uint8_t block[16];

        copy_block(block, in + i);
        decrypt_block(block, expanded_key);
        copy_block(out + i, block);
    }

    return len;
}

int main(void) {
    WSADATA wsa;

    if (WSAStartup(MAKEWORD(2, 2), &wsa) != 0) {
        printf("WSAStartup failed\n");
        return 1;
    }

    uint8_t expanded_key[176];

    key_expansion(key, expanded_key);

    SOCKET server_sock = socket(AF_INET, SOCK_STREAM, 0);

    if (server_sock == INVALID_SOCKET) {
        printf("socket failed: %d\n", WSAGetLastError());
        WSACleanup();
        return 1;
    }

    struct sockaddr_in server_addr;

    memset(&server_addr, 0, sizeof(server_addr));

    server_addr.sin_family = AF_INET;
    server_addr.sin_port = htons(PORT);
    server_addr.sin_addr.s_addr = INADDR_ANY;

    if (bind(server_sock,
             (struct sockaddr *)&server_addr,
             sizeof(server_addr)) == SOCKET_ERROR) {

        printf("bind failed: %d\n", WSAGetLastError());

        closesocket(server_sock);
        WSACleanup();

        return 1;
    }

    if (listen(server_sock, 1) == SOCKET_ERROR) {
        printf("listen failed: %d\n", WSAGetLastError());

        closesocket(server_sock);
        WSACleanup();

        return 1;
    }

    printf("Listening on port %d...\n", PORT);

    struct sockaddr_in client_addr;
    int client_len = sizeof(client_addr);

    SOCKET client_sock = accept(
        server_sock,
        (struct sockaddr *)&client_addr,
        &client_len
    );

    if (client_sock == INVALID_SOCKET) {
        printf("accept failed: %d\n", WSAGetLastError());

        closesocket(server_sock);
        WSACleanup();

        return 1;
    }

    printf("Client connected!\n");

    uint8_t buffer[BUFFER_SIZE];
    uint8_t plain[BUFFER_SIZE + 1];

    while (1) {
        int bytes = recv(client_sock, (char *)buffer, BUFFER_SIZE, 0);

        if (bytes <= 0) {
            printf("Client disconnected\n");
            break;
        }

        int plain_len =
            decrypt_buffer(buffer, plain, bytes, expanded_key);

        if (plain_len < 0) {
            printf("Encrypted packet length must be a multiple of 16\n");
            continue;
        }

        plain[plain_len] = '\0';

        if (plain_len < 3) {
            printf("Received packet too short\n");
            continue;
        }

        unsigned char type = plain[0];
        unsigned short len = (plain[1] << 8) | plain[2];

        if (len > plain_len - 3) {
            printf(
                "Incomplete payload: expected %u, got %d\n",
                len,
                plain_len - 3
            );

            continue;
        }

        printf(
            "Packet Type: %u, Length: %u, Payload: %.*s\n",
            type,
            len,
            len,
            (char *)(plain + 3)
        );
    }

    closesocket(client_sock);
    closesocket(server_sock);

    WSACleanup();

    return 0;
}
